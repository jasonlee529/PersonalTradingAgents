def _load_flow_log(log_path: Path) -> Any:
    """Load the full_states_log JSON file (sync, meant for thread pool)."""
    if not log_path.exists():
        return None
    with open(log_path, "r", encoding="utf-8") as fh:
        return json.load(fh)


def _normalize_status(status: str) -> str:
    return "completed" if status == "done" else status


def _job_to_response(job) -> AnalysisStatusResponse:
    return AnalysisStatusResponse(
        job_id=job.id,
        symbol=job.symbol,
        status=_normalize_status(job.status.value),
        phase=job.phase,
        progress=job.progress,
        result_summary=job.result_summary or "",
        error=job.error or "",
        steps=[
            AnalysisStepItem(
                step_id=s.step_id,
                label=s.label,
                role=s.role,
                character=s.character,
                module=s.module,
                action=s.action,
                artifact_key=s.artifact_key,
                status=s.status.value,
                started_at=s.started_at.isoformat() if s.started_at else None,
                completed_at=s.completed_at.isoformat() if s.completed_at else None,
                detail=s.detail,
            )
            for s in job.steps
        ],
        created_at=job.created_at.isoformat() if job.created_at else None,
        output_files=job.output_files,
    )


@router.post("/")
async def start_analysis(body: AnalysisRequest, services: AppServices = Depends(get_services)):
    """Create a pending analysis job. The actual execution is handled by the background worker."""
    config = {}
    if body.output_language:
        config["output_language"] = body.output_language
    if body.llm_provider:
        config["llm_provider"] = body.llm_provider
    if body.analysts:
        config["analysts"] = body.analysts
    if body.research_depth:
        config["research_depth"] = body.research_depth
    if body.thinking_agents is not None:
        config["thinking_agents"] = body.thinking_agents
    if body.trade_date:
        config["trade_date"] = body.trade_date
    if body.checkpoint_enabled is not None:
        config["checkpoint_enabled"] = body.checkpoint_enabled

    job = AnalysisJob(
        id=str(uuid.uuid4())[:8],
        symbol=body.symbol,
        config=config if config else None,
    )
    await services.job_store.save(job)
    return _job_to_response(job)


